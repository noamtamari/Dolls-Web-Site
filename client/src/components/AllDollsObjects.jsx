export function getDolls() {
    return dolls;
}

export function getDoll(name) {
    const selectedDolls = [];

    for (const category of dolls) {
        // console.log(name+" "+category.name);
        if (category.name === name) {

            return getSubCategories(name);

        }
        for (const subCategory of category.subCategories) {
            if (subCategory.name === name) {
                selectedDolls.push(...subCategory.dolls);
            }
        }
    }
    console.log(selectedDolls);
    return selectedDolls;
}

export function getDollObject(dollName) {
    const doll = dolls
        .flatMap((category) => category.subCategories)
        .flatMap((subCategory) => subCategory.dolls)
        .find((doll) => doll.name === dollName);

    return doll || null; // Doll not found
};


export function getCategories() {
    const categories = dolls.map((category) => category.name);
    return categories;
}
export function getSubCategories(category) {
    const selectedCategory = dolls.find((item) => item.name === category);

    if (selectedCategory) {
        const subCategories = selectedCategory.subCategories.map((subCategory) => subCategory.name);
        return subCategories;
    } else {
        return [];
    }
}

export function getCategoryImage(categoryName) {
    const selectedCategory = dolls.find((doll) => doll.name === categoryName);
    if (selectedCategory) {
        return selectedCategory.categoryImage;
    } else {
        return null; // Or you can return a default image if the category is not found
    }
}

export function getSubCategoryImage(categoryName) {
    for (const category of dolls) {
        const subCategory = category.subCategories.find(
            (subCategory) => subCategory.name === categoryName
        );
        if (subCategory) {
            return subCategory.subCategoryImage;
        }
    }
    return null; // Return a default image or handle the case when the subcategory is not found
}

export function getSubCategoryDolls(categoryName) {
    for (const category of dolls) {
        const subCategory = category.subCategories.find(
            (subCategory) => subCategory.name === categoryName
        );
        if (subCategory) {
            return subCategory.dolls;
        }
    }
    return null; // Return a default image or handle the case when the subcategory is not found
}
export function getSubCategoryDollsNum(categoryName) {
    for (const category of dolls) {
        const subCategory = category.subCategories.find(
            (subCategory) => subCategory.name === categoryName
        );
        if (subCategory) {
            return subCategory.dolls.length;
        }
    }
    return null;
}


export default dolls;